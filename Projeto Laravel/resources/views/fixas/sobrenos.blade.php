                <h1>Surgimento</h1>
                <p class="text-justify"><br/>
                    Há pouco tempo, quem quisesse estudar outra língua teria que pagar valores exorbitantes, curso de idiomas “era para rico”, que queria ser “doutor” ou “morar no exterior”. Na atualidade não está tão diferente (com relação aos valores), porém está mais acessível, principalmente devido ao avanço tecnológico, softwares, músicas, filmes, etc.<br/><br/>Seguindo esse preceito de que: "só podia fazer um curso de idiomas quem possuía condições financeiras favoráveis” foi o que motivou o professor de Inglês Ricardo Vilaronga,  a desenvolver o projeto do Vilaronga Cursos e Idiomas, misto de escola e projeto social, que surgiu como uma alternativa para o compartilhamento de conhecimentos, direcionado a todo público interessado, independente de classe social. <br /><br /><br /><br /><br /></p>
