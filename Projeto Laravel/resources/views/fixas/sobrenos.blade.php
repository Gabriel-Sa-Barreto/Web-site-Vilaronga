                <p class="text-justify"><br /><br />
                    Ricardo é graduado em Letras com Língua Inglesa pela Universidade Estadual de Feira de SantanaBA (UEFS) e pós-graduado em Ensino de Língua Inglesa pela Universidade Cândido Mendes (UCAM). Tem vasta experiência no ensino de Língua Inglesa, assim também como no ramo de Traduções de Documentos de cunho Secreto, Judicial, Administrativo, Prestação de serviços de Tradução, Interpretação, Revisão e correlatos para pessoas físicas e diversas Empresas. Voltando ainda aos conhecimentos adquiridos no decorrer de sua formação, Ricardo desenvolveu vários projetos voltados à aquisição da Língua Inglesa nas instituições por onde passou; sempre desenvolvendo uma perspectiva de ampliar o ensino aprendizagem do Inglês direcionado a realidade do aluno, seu meio social e, portanto, levando em consideração seu conhecimento prévio.
                    <br /><br />
                    Ricardo acredita que o incentivo e a preocupação com o aluno é muito importante, pois a motivação faz parte do processo. "Em um primeiro momento é preciso quebrar alguns modismos e preconceitos de que Inglês é difícil. Independente da idade, qualquer pessoa poderá aprender, porém a conduta do aluno é que será um fator preponderante." Para alimentar o interesse no aluno, o Vilaronga promove trimestralmente projetos em áreas específicas, como músicas, filmes, culinária, viagens, regiões geográficas, etc, além de composições, apresentações, conversação (desde o curso básico) espontânea e distinções fonéticas e fonológicas. Encaminhando para o 4º ano de atividade, o Vilaronga já fez e continua fazendo a diferença na vida de mais de 400 pessoas (crianças, jovens e adultos). O valor da mensalidade é a partir R$ 40,00. O valor da apostila  custa  a partir de R$ 30,00; é cobrado apenas o valor da impressão e da encadernação. Os demais materiais (cópias, impressões, artigos, atividades) o curso não cobra, pois já está incluso no valor mensal que o aluno paga. O curso tem duração aproximada de 14 meses para cada nível. Ao término de cada fase o aluno recebe o certificado, sem custos adicionais. Além de Inglês, o Vilaronga oferece curso de Língua Espanhola e Português/ Redação, voltados para Concursos, Vestibulares e Enem.<br /><br /></p>
                <h1>Responsabilidade Social</h1>
                <p class="text-justify"><br/>Como o tema sugere, nossa instituição tem como pressuposto a evidente contribuição a toda a
                    sociedade de forma ética e responsável, vinculado a comportamentos e ações a fim de promulgar a
                    ascensão e o bem estar não apenas dos alunos, mas também dos colaboradores e do corpo social.<br/><br/>
                    
                    Tendo em vista essas metas, o Vilaronga Cursos e Idiomas propõe a imersão dos estudantes no
                    mundo globalizado. É indiscutível a necessidade de se manter atualizado nesse sistema cada vez mais desafiador. Estamos conectados todo o tempo e precisamos equilibrar nossas ações, conhecer
                    nossa língua é “obrigação”, mas adquirir, desenvolver e aprender outros idiomas é uma atividade imposta pelo atual cenário mundial. Diante disso, num movimento em cadeia, todas as pessoas direta 
                    ou indiretamente são afetadas pela falta ou não de comunicação de outro(s) idioma(s).<br/><br/>
                    
                    Assim sendo, nossa conduta resulta numa constante consolidação de uma abordagem linear, de maneira que quando se procura entender a necessidade do próximo estamos compartilhando, aprendendo e contribuindo socialmente. Nesse contexto, o maior marketing dos nossos cursos é realizado por nossos alunos, que apresentam a seus amigos e familiares. Mediante essa perspectiva mútua é que estamos, há mais de 3 anos, engajados nesse projeto.<br/><br/>
                    
                    Portanto, nós nos preocupamos com a realidade tecnológica a qual estamos inseridos dentro do contexto globalizado. Esse é o nosso princípio, incentivar e dividir as experiências com toda a comunidade, independente de classe social, para que todas as pessoas tenham maior facilidade quanto à conquista de seus objetivos.<br/><br/>
                </p>
                <h1>Relacionamento com o aluno</h1>
                <p class="text-justify"><br/>Um dos fatores de destaque pelo qual prezamos muito é a reciprocidade no que diz respeito ao acolhimento e ao acompanhamento dos nossos alunos. O relacionamento é próximo e ético, de
                    maneira que todos se sentem mais a vontade nas aulas. Como incentivo, presenteamos-lhes com livros, gramáticas e brindes, principalmente aos que se destacam nas atividades propostas.
                    Assim sendo, há também dentro de uma perspectiva responsável a atenção com a frequência dos nossos discentes. Existe a preocupação do planejamento das aulas, das atividades, assim também
                    como o suporte dos exercícios extraclasse.<br/><br/>
                    
                    Geralmente quando alguém falta à aula entramos em contato a fim de termos o retorno, o porquê da ausência. Essa atitude deixa evidente o nosso cuidado e o respeito para com nossos alunos.<br/><br/>
                </p>
