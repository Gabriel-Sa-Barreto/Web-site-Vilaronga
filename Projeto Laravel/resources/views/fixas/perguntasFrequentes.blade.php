                <h5 style="color: blue;">Qual o endereço do Vilaronga Cursos e Idiomas?</h5>
                <p class="text-justify">
                    <b>Avenida Getúlio Vargas, (no mesmo prédio) Hotel Nordeste, em frente à Praça de Alimentação, no
                    1° andar, sala 107.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Qual o objetivo do Vilaronga Cursos e Idiomas?</h5>
                <p class="text-justify">
                    <b>Nosso objetivo é compartilhar o conhecimento a um custo acessível, independente de classe
                    social</b><br/><br/>
                </p>


                <h5 style="color: blue;">Qual a duração de cada curso?</h5>
                <p class="text-justify">
                    <b>O curso dura em média 14 meses (levando em consideração feriados e recessos de meio e final
                    de ano).</b><br/><br/>
                </p>

                <h5 style="color: blue;">Como é o material didático utilizado pelo Vilaronga Cursos e Idiomas?</h5>
                <p class="text-justify">
                    <b>O material é planejado e preparado em conjunto com os professores colaboradores.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Qual a idade mínima para iniciar os cursos de Inglês e Espanhol?</h5>
                <p class="text-justify">
                    <b>A partir dos 8 anos.</b><br/><br/>
                </p>

                <h5 style="color: blue;">O curso aplica teste de Nivelamento?</h5>
                <p class="text-justify">
                    <b>Sim, é realizado teste de nivelamento para os cursos de Inglês e Espanhol (principalmente para
                    alunos que já tenham uma base e pretendem se matricular no nível seguinte).</b><br/><br/>
                </p>

                <h5 style="color: blue;">Qual a Metodologia utilizada pelo Vilaronga Cursos e Idiomas?</h5>
                <p class="text-justify">
                    <b>Utilizamos metodologia diferenciada, de maneira que levamos em consideração o conhecimento
                    prévio do aluno. Trabalhamos com áudios, filmes, trailers, powerpoint, datashow, aplicativos. Nos
                    cursos de idiomas temos materiais específicos e voltados para as seguintes áreas da aprendizagem:
                    leitura, escrita, audição, fala e gramática.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Qual o critério para avaliação dos alunos?</h5>
                <p class="text-justify">
                    <b>Trabalhamos por trimestres e o processo de avaliação é contabilizado aula a aula (contínuo), é
                    feita uma avaliação final a cada término de trimestre. Mínimo/média de aproveitamento de 70% por
                    trimestre.</b><br/><br/>
                </p>

                <h5 style="color: blue;">É preciso ter uma frequência mínima para aprovação nos cursos?</h5>
                <p class="text-justify">
                    <b>Sim. Para aprovação nos cursos é necessário no mínimo 75% de frequência.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Os cursos têm Certificados?</h5>
                <p class="text-justify">
                    <b>Sim. Todos os alunos recebem o Certificado ao término do curso.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Os Certificados são reconhecidos pelo MEC?</h5>
                <p class="text-justify">
                    <b>O MEC não é o órgão responsável por regulamentar curso de idiomas. Todos os nossos cursos
                    têm autorização e certificado reconhecido em todo território nacional.</b><br/><br/>
                </p>

                <h5 style="color: blue;">Como faço para saber o valor do curso que tenho interesse?</h5>
                <p class="text-justify">
